    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
         TareasJDBC tareasjdbc = null;
        try {
            tareasjdbc = new TareasJDBC();
        } catch (Exception ex) {
            Logger.getLogger(pantallaAgregarTareas.class.getName()).log(Level.SEVERE, null, ex);
        }
        String nombreTarea, fechainicio, fechalimite;
        fechainicio = sdf.format(nomInicio.getDate());
        fechalimite = sdf.format(nomLimite.getDate());
        nombreTarea = nomNombre.getText();
        int estado = 1;
        tareasjdbc.insert(nombreTarea, fechainicio, fechalimite, jTextArea1.getText(), estado);
        AgregarAcciones newFrame = null;
        try {
            newFrame = new AgregarAcciones();
        } catch (Exception ex) {
            Logger.getLogger(pantallaAgregarTareas.class.getName()).log(Level.SEVERE, null, ex);
        }
        newFrame.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_jButton2ActionPerformed

